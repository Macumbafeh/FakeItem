-- create the enchantment menu - uses recursion for building each submenu
-- the parent is the menuList of the menu item
-- the prefix is added onto with each submenu so that a full name is known for each choosable menu item option
local menuTableOnSuffixName
local function AddSuffixMenuTable(onTable, parent, prefix)
	parent = parent or FakeItem.suffixMenu -- if no parent, then start at the root of the menu
	prefix = prefix or ""

	for i=1,#onTable do
		if type(onTable[i]) == "string" then
			parent[i] = {text=onTable[i], isTitle=1, notCheckable=1}
		else
			if onTable[i][1]:sub(1, 3) == "of " or onTable[i][1] == "Stat Sta 15" then
				menuTableOnSuffixName = onTable[i][1]
			end
			if type(onTable[i][2]) == "number" then
				-- set the menu item as a choosable option - sends the fully built name and ID as function parameters
				parent[i] = {text=onTable[i][1], notCheckable=1, func=FakeItem.SetSuffixFromMenu, value=onTable[i][2], arg1=prefix..onTable[i][1], arg2=menuTableOnSuffixName}
			else
				-- set the menu item as a submenu
				parent[i] = {text=onTable[i][1], notCheckable=1, hasArrow=1, notClickable=1, menuList = {}}
				-- build the submenu - this item's menuList will be the parent and append this item's name as a prefix for items in the submenu
				AddSuffixMenuTable(onTable[i][2], parent[i].menuList, prefix..onTable[i][1]..">")
			end
		end
	end
	if parent == FakeItem.suffixMenu then
		parent[#parent+1] = {text="Close", notCheckable=1}
	end
end

-- create the enchantment menu - uses recursion for building each submenu
-- the parent is the menuList of the menu item
-- the prefix is added onto with each submenu so that a full name is known for each choosable menu item option
local function AddEnchantmentMenuTable(onTable, parent, prefix)
	parent = parent or FakeItem.enchantmentMenu -- if no parent, then start at the root of the menu
	prefix = prefix or ""

	for i=1,#onTable do
		-- if the current item's value is a number then it must be a choosable menu item - if not then it's a table describing a submenu
		if type(onTable[i][2]) == "number" then
			-- set the menu item as a choosable option - sends the fully built name and the item's ID value as function parameters
			parent[i] = {text=onTable[i][1], notCheckable=1, func=FakeItem.SetEnchantmentFromMenu, arg1=prefix..onTable[i][1], arg2=onTable[i][2]}
		else
			-- set the menu item as a submenu
			parent[i] = {text=onTable[i][1], notCheckable=1, hasArrow=true, notClickable=true, menuList = {}}
			-- build the submenu - this item's menuList will be the parent and append this item's name as a prefix for items in the submenu
			AddEnchantmentMenuTable(onTable[i][2], parent[i].menuList, prefix..onTable[i][1]..">")
		end
	end
	if parent == FakeItem.enchantmentMenu then
		parent[#parent+1] = {text="Close", notCheckable=1}
	end
end

